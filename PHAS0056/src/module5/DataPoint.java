package module5;


public class DataPoint {

	double x;
	double y;
	double ey;
	
	public DataPoint(double x, double y, double ey) {
		
	}
	// gets the x value
	public double getX() {
		return x;
	}
	// gets the y value
	public double getY() {
		return y;
	}
	// gets the error on the y value
	public double getEY() {
		return ey;
	}
	
	
}